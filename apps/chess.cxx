extern std::optional<UI::Desktop> g_Desktop;
__attribute__((section(".text.startup"))) int UDOS_32Main(char32_t[])
{
    struct Playfield
    {
        enum CellType
        {
            EMPTY,
            RESERVED_1,
            BLACK_PAWN,
            BLACK_QUEEN,
            BLACK_KING,
            BLACK_BISHOP,
            BLACK_TOWER,
            BLACK_HORSE,
            WHITE_PAWN,
            WHITE_QUEEN,
            WHITE_KING,
            WHITE_BISHOP,
            WHITE_TOWER,
            WHITE_HORSE,
        };

        struct Cell
        {
            CellType type = CellType::EMPTY;
            bool hasDoneFirstMove = false;
        };

        Playfield(size_t _cols, size_t _rows)
            : cols{_cols},
              rows{_rows}
        {
            this->cells.resize(this->cols * this->rows);
            std::vector<CellType> cellsTypes{
                BLACK_TOWER, BLACK_HORSE, BLACK_BISHOP, BLACK_QUEEN, BLACK_KING, BLACK_BISHOP, BLACK_HORSE, BLACK_TOWER,
                BLACK_PAWN, BLACK_PAWN, BLACK_PAWN, BLACK_PAWN, BLACK_PAWN, BLACK_PAWN, BLACK_PAWN, BLACK_PAWN,
                EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY,
                EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY,
                EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY,
                EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY, EMPTY,
                WHITE_PAWN, WHITE_PAWN, WHITE_PAWN, WHITE_PAWN, WHITE_PAWN, WHITE_PAWN, WHITE_PAWN, WHITE_PAWN,
                WHITE_TOWER, WHITE_HORSE, WHITE_BISHOP, WHITE_QUEEN, WHITE_KING, WHITE_BISHOP, WHITE_HORSE, WHITE_TOWER};

            for (size_t i = 0; i < cellsTypes.size(); i++)
                this->cells[i].type = cellsTypes[i];
            this->legalMoves.resize(this->cols * this->rows);
        }

        /// @brief Checks if we can go to the given coordinates of tiles
        /// use only IFF we're not a pawn
        bool CanGotoTileNotPawn(int i, int j, bool isWhite)
        {
            if (!this->IsPiece(this->GetCell(i, j))) // Must be clear
            {
                this->legalMoves[i + j * this->cols] = true;
                return true;
            }
            else
            {
                if (this->IsPiece(this->GetCell(i, j), isWhite))
                { // Our piece (can't be jumped over)
                    return false;
                }
                else // Enemy piece (can be destroyed)
                {
                    this->legalMoves[i + j * this->cols] = true;
                    return false;
                }
            }
            return false;
        }

        /// @brief Special handling for king
        bool CanGotoTileKing(int i, int j, bool isWhite)
        {
            if (!this->IsPiece(this->GetCell(i, j))) // Must be clear
            {
                this->legalMoves[i + j * this->cols] = true;
                return true;
            }
            else
            {
                if (this->IsPiece(this->GetCell(i, j), isWhite))
                { // Our piece (can't be jumped over)
                    const auto castleType = isWhite ? CellType::WHITE_TOWER : CellType::BLACK_TOWER;
                    return this->GetCell(i, j).type == castleType; // Castling
                }
                else // Enemy piece (can be destroyed)
                {
                    this->legalMoves[i + j * this->cols] = true;
                    return false;
                }
            }
            return false;
        }

        std::vector<bool> &GetLegalMoves(size_t cx, size_t cy, bool isWhite)
        {
            std::fill(this->legalMoves.begin(), this->legalMoves.end(), false);

            const auto &cell = this->GetCell(cx, cy);
            const auto doPawnMove = [&](int up)
            {
                // Can't capture horizontally, only with En Passant
                if (this->IsValidCoord(cx, cy, 0, up) && !this->IsPiece(this->GetCell(cx, cy + up)))
                {
                    if (!cell.hasDoneFirstMove) // Move two spaces at first turn of pawn
                        if (this->IsValidCoord(cx, cy, 0, up * 2) && !this->IsPiece(this->GetCell(cx, cy + up * 2)))
                            this->legalMoves[cx + (cy + up * 2) * this->cols] = true;
                    this->legalMoves[cx + (cy + up) * this->cols] = true; // Just move
                }

                // En passant (right)
                const auto enPassant = [&](int side)
                {
                    if (this->IsValidCoord(cx, cy, side, up) && !this->IsPiece(this->GetCell(cx + side, cy + up)))
                        // Can only en-passant other's pieces
                        if (this->IsValidCoord(cx, cy, side, 0) && this->IsPiece(this->GetCell(cx + side, cy), !isWhite))
                            this->legalMoves[cx + side + (cy + up) * this->cols] = true; // Just move
                };

                enPassant(+1); // Right
                enPassant(-1); // Left
            };

            const auto doBishopMove = [&]()
            {
                // Diagonal (left top)
                for (int i = (int)cx - 1, j = (int)cy - 1; i >= 0 && j >= 0; i--, j--)
                    if (!this->CanGotoTileNotPawn(i, j, isWhite))
                        break;
                // Diagonal (right top)
                for (int i = (int)cx - 1, j = (int)cy + 1; i >= 0 && j < (int)this->rows; i--, j++)
                    if (!this->CanGotoTileNotPawn(i, j, isWhite))
                        break;
                // Left bottom
                for (int i = (int)cx + 1, j = (int)cy - 1; i < (int)this->cols && j >= 0; i++, j--)
                    if (!this->CanGotoTileNotPawn(i, j, isWhite))
                        break;
                // Right bottom
                for (int i = (int)cx + 1, j = (int)cy + 1; i < (int)this->cols && j < (int)this->rows; i++, j++)
                    if (!this->CanGotoTileNotPawn(i, j, isWhite))
                        break;
            };

            const auto doTowerMove = [&]()
            {
                // Can only move up/down/left/right
                for (int i = (int)cx - 1; i >= 0; i--) // Left side
                    if (!this->CanGotoTileNotPawn(i, cy, isWhite))
                        break;
                for (int i = (int)cx + 1; i < this->cols; i++) // Right side
                    if (!this->CanGotoTileNotPawn(i, cy, isWhite))
                        break;
                for (int i = (int)cy - 1; i >= 0; i--) // Up side
                    if (!this->CanGotoTileNotPawn(cx, i, isWhite))
                        break;
                for (int i = (int)cy + 1; i < this->rows; i++) // Down side
                    if (!this->CanGotoTileNotPawn(cx, i, isWhite))
                        break;
            };

            const auto doKingMove = [&]()
            {
                // Can only move 1 square at a time
                if ((int)cx - 1 >= 0) // Left side
                    this->CanGotoTileNotPawn(cx - 1, cy, isWhite);
                if ((int)cx + 1 < this->cols) // Right side
                    this->CanGotoTileNotPawn(cx + 1, cy, isWhite);
                if ((int)cy - 1 >= 0) // Up side
                    this->CanGotoTileNotPawn(cx, cy - 1, isWhite);
                if ((int)cy + 1 < this->rows) // Down side
                    this->CanGotoTileNotPawn(cx, cy + 1, isWhite);
            };

            const auto doHorseMove = [&]()
            {
                // 3-up, 1- left/right
                const auto checkLorR = [&](const int finalCy)
                {
                    if (this->IsValidCoord(cx, finalCy, -1, 0) && this->CanGotoTileNotPawn(cx - 1, finalCy, isWhite)) // Left
                        this->legalMoves[cx - 1 + finalCy * this->cols] = true;
                    if (this->IsValidCoord(cx, finalCy, +1, 0) && this->CanGotoTileNotPawn(cx + 1, finalCy, isWhite)) // Right
                        this->legalMoves[cx + 1 + finalCy * this->cols] = true;
                };
                int finalCy = cy + 2;
                if (this->IsValidCoord(cx, finalCy, 0, 0))
                    checkLorR(finalCy);
                finalCy = cy - 2;
                if (this->IsValidCoord(cx, finalCy, 0, 0))
                    checkLorR(finalCy);

                // L-shape for Left or Right
                const auto checkUorD = [&](const int finalCx)
                {
                    if (this->IsValidCoord(finalCx, cy, 0, -1) && this->CanGotoTileNotPawn(finalCx, cy - 1, isWhite)) // Left
                        this->legalMoves[finalCx + (cy - 1) * this->cols] = true;
                    if (this->IsValidCoord(finalCx, cy, 0, +1) && this->CanGotoTileNotPawn(finalCx, cy + 1, isWhite)) // Right
                        this->legalMoves[finalCx + (cy + 1) * this->cols] = true;
                };
                int finalCx = cx + 2;
                if (this->IsValidCoord(finalCx, cy, 0, 0))
                    checkUorD(finalCx);
                finalCx = cx - 2;
                if (this->IsValidCoord(finalCx, cy, 0, 0))
                    checkUorD(finalCx);
            };

            // NOT: Black starts at top, white is at bottom
            switch (cell.type)
            {
            case CellType::BLACK_PAWN:
                doPawnMove(+1);
                break;
            case CellType::WHITE_PAWN:
                doPawnMove(-1);
                break;
            case CellType::BLACK_BISHOP:
            case CellType::WHITE_BISHOP:
                doBishopMove();
                break;
            case CellType::BLACK_TOWER:
            case CellType::WHITE_TOWER:
                doTowerMove();
                break;
            case CellType::BLACK_QUEEN:
            case CellType::WHITE_QUEEN:
                doBishopMove();
                doTowerMove();
                break;
            case CellType::BLACK_KING:
            case CellType::WHITE_KING:
                doKingMove();
                break;
            case CellType::BLACK_HORSE:
            case CellType::WHITE_HORSE:
                doHorseMove();
                break;
            default:
                break;
            }
            return this->legalMoves;
        }

        bool IsValidCoord(size_t cx, size_t cy, int ox, int oy)
        {
            if ((int)cx + ox >= (int)this->cols || (int)cy + oy >= (int)this->rows)
                return false;
            if ((int)cx - ox < 0 || (int)cy - oy < 0)
                return false;
            return true;
        }

        bool IsPiece(Cell &cell)
        {
            return cell.type != CellType::EMPTY;
        }

        bool IsPiece(Cell &cell, bool isWhite)
        {
            switch (cell.type)
            {
            case CellType::BLACK_BISHOP:
            case CellType::BLACK_HORSE:
            case CellType::BLACK_KING:
            case CellType::BLACK_PAWN:
            case CellType::BLACK_QUEEN:
            case CellType::BLACK_TOWER:
                return !isWhite;
            case CellType::WHITE_BISHOP:
            case CellType::WHITE_HORSE:
            case CellType::WHITE_KING:
            case CellType::WHITE_PAWN:
            case CellType::WHITE_QUEEN:
            case CellType::WHITE_TOWER:
                return isWhite;
            default:
                break;
            }
            return false;
        }

        bool MovePiece(size_t fx, size_t fy, size_t tx, size_t ty, bool isWhite)
        {
            this->GetLegalMoves(fx, fy, isWhite);
            if (this->IsValidCoord(tx, ty, 0, 0))
            {
                // Must be a piece
                if (!this->IsPiece(this->GetCell(fx, fy), isWhite))
                    return false;

                if (this->legalMoves[tx + ty * this->cols])
                {
                    auto &fromCell = this->GetCell(fx, fy);
                    fromCell.hasDoneFirstMove = true; // Deny for pawns :)
                    auto &toCell = this->GetCell(tx, ty);

                    // Castling
                    if ((toCell.type == CellType::WHITE_TOWER && fromCell.type == CellType::WHITE_KING) || (toCell.type == CellType::BLACK_TOWER && fromCell.type == CellType::BLACK_KING))
                    {
                        const auto tmp = toCell;
                        toCell = fromCell;
                        fromCell = tmp;
                    }
                    else if (toCell.type == CellType::WHITE_PAWN)
                    { // En passant
                        if (this->IsValidCoord(tx, ty, 0, +1))
                        {
                            toCell = fromCell; // Move piece
                            fromCell = Cell{}; // Reset the from cell
                            this->GetCell(tx, ty + 1) = Cell{};
                        }
                    }
                    else if (toCell.type == CellType::BLACK_PAWN)
                    { // En passant
                        if (this->IsValidCoord(tx, ty, 0, -1))
                        {
                            toCell = fromCell; // Move piece
                            fromCell = Cell{}; // Reset the from cell
                            this->GetCell(tx, ty - 1) = Cell{};
                        }
                    }
                    else
                    {
                        toCell = fromCell; // Move piece
                        fromCell = Cell{}; // Reset the from cell
                    }

                    // Promote to queen once they reach the other end
                    if (toCell.type == CellType::WHITE_PAWN && ty == 0)
                        toCell.type = CellType::WHITE_QUEEN;
                    else if (toCell.type == CellType::BLACK_PAWN && ty == this->rows - 1)
                        toCell.type = CellType::BLACK_QUEEN;
                    return true;
                }
            }
            return false;
        }

        Cell &GetCell(size_t cx, size_t cy)
        {
            return this->cells[cx + cy * this->cols];
        }

        const Cell &GetCell(size_t cx, size_t cy) const
        {
            return this->cells[cx + cy * this->cols];
        }

        std::vector<bool> legalMoves;
        std::vector<Cell> cells;
        size_t cols, rows;

        bool selected = false;
        size_t selectedX, selectedY;
    };
    static Playfield gamePlayfield(8, 8);

    auto &gameWindow = g_Desktop->AddChild<UI::Window>();
    gameWindow.SetText("Chess");

    struct PlayfieldWidget : UI::Viewport
    {
        virtual void Draw()
        {
            // Draw our playfield
            for (size_t tx = 0; tx < gamePlayfield.cols; tx++)
            {
                for (size_t ty = 0; ty < gamePlayfield.rows; ty++)
                {
                    const auto &cell = gamePlayfield.cells[tx + ty * gamePlayfield.cols];

                    // Tiled board pattern, easy to draw
                    size_t spriteIdx = ((tx == 0 ? 2 : tx) + (ty * (gamePlayfield.cols + 1))) % 2 == 0 ? 0 : 1;
                    this->DrawSprite(tx, ty, spriteIdx);

                    if (cell.type != Playfield::CellType::EMPTY)
                    {
                        spriteIdx = static_cast<size_t>(cell.type); // Object itself
                        this->DrawSprite(tx, ty, spriteIdx);
                    }

                    // Highlight with green
                    if (gamePlayfield.legalMoves[tx + ty * gamePlayfield.cols])
                        this->DrawSprite(tx, ty, 14);
                }
            }
        }

        void DrawSprite(const int tpx, const int tpy, const int tile_idx)
        {
            constexpr size_t tw = 32; // Tile width and height
            constexpr size_t th = 32;
            const size_t to = tile_idx * tw * th;
            for (size_t tx = 0; tx < tw; tx++)
            {
                for (size_t ty = 0; ty < th; ty++)
                {
                    Color pixel{};
                    pixel.r = gameSpriteColorMap[gameSpriteData[to + tx + ty * tw]][2];
                    pixel.g = gameSpriteColorMap[gameSpriteData[to + tx + ty * tw]][1];
                    pixel.b = gameSpriteColorMap[gameSpriteData[to + tx + ty * tw]][0];
                    if (pixel.b == 255 && pixel.g == 0 && pixel.r == 253)
                        continue;
                    g_KFrameBuffer.PlotPixel(this->ox + (tpx * tw) + tx, this->oy + (tpy * th) + ty, pixel);
                }
            }
        }

        void DrawFillTile(const int tpx, const int tpy, Color color)
        {
            constexpr size_t tw = 32; // Tile width and height
            constexpr size_t th = 32;
            for (size_t tx = 0; tx < tw; tx++)
                for (size_t ty = 0; ty < th; ty++)
                    g_KFrameBuffer.PlotPixel(this->ox + (tpx * tw) + tx, this->oy + (tpy * th) + ty, color);
        }
    };

    static auto &gameViewport = gameWindow.AddChild<PlayfieldWidget>();
    gameViewport.x = gameViewport.y = 0;
    gameViewport.width = 32 * gamePlayfield.cols;
    gameViewport.height = 32 * gamePlayfield.rows;
    gameViewport.OnClick = [](UI::Widget &w, unsigned int mx, unsigned int my, bool, bool)
    {
        static uint16_t randSeed = 0x5a7f;
        size_t cx = mx >> 5, cy = my >> 5;

        if (!gamePlayfield.IsValidCoord(cx, cy, 0, 0))
            return; // Invalid selection

        randSeed += 23237 + 23813 * (static_cast<uint16_t>(mx >> 5) * static_cast<uint16_t>(my >> 5));
        if (gamePlayfield.selected)
        {
            // Move from to
            if (gamePlayfield.MovePiece(gamePlayfield.selectedX, gamePlayfield.selectedY, cx, cy, true))
            {
                gamePlayfield.selected = false;

                // Random AI
                bool couldMakeMove = false;
                for (size_t acx = 0; acx < gamePlayfield.cols; acx++)
                {
                    for (size_t acy = 0; acy < gamePlayfield.rows; acy++)
                    {
                        randSeed += 23237 + 23813 * (static_cast<uint16_t>(mx >> 5) * static_cast<uint16_t>(my >> 5));
                        if (gamePlayfield.IsPiece(gamePlayfield.GetCell(acx, acy), false))
                        {
                            const auto& legalMoves = gamePlayfield.GetLegalMoves(acx, acy, false);
                            for (size_t ix = 0; ix < gamePlayfield.cols; ix++)
                            {
                                for (size_t iy = 0; iy < gamePlayfield.rows; iy++)
                                {
                                    if (randSeed % (ix * iy) < 32 && gamePlayfield.MovePiece(acx, acy, ix, iy, false))
                                    {
                                        couldMakeMove = true;
                                        break;
                                    }
                                }
                                if (couldMakeMove)
                                    break;
                            }
                            if (couldMakeMove)
                                break;
                        }
                    }
                    if (couldMakeMove)
                        break;
                }

                if (!couldMakeMove)
                {
                    // Bruteforce it
                    for (size_t acx1 = 0; acx1 < gamePlayfield.cols; acx1++)
                    {
                        for (size_t acy1 = 0; acy1 < gamePlayfield.rows; acy1++)
                        {
                            for (size_t acx2 = 0; acx2 < gamePlayfield.cols; acx2++)
                            {
                                for (size_t acy2 = 0; acy2 < gamePlayfield.rows; acy2++)
                                {
                                    if (gamePlayfield.MovePiece(acx1, acy1, acx2, acy2, false))
                                    {
                                        couldMakeMove = true;
                                        break;
                                    }
                                }
                                if (couldMakeMove)
                                    break;
                            }
                            if (couldMakeMove)
                                break;
                        }
                        if (couldMakeMove)
                            break;
                    }

                    if (!couldMakeMove)
                    {
                        UI::Manager::Get().ErrorMsg(*"You win", *"You win this Chess match :(");
                    }
                }

                std::fill(gamePlayfield.legalMoves.begin(), gamePlayfield.legalMoves.end(), false);
                w.Redraw();
                return;
            }
            else
            {
                gamePlayfield.selected = false;
                w.Redraw();
            }
        }

        if (!gamePlayfield.selected)
        {
            // There must be a piece here
            if (gamePlayfield.IsPiece(gamePlayfield.GetCell(cx, cy), true))
            {
                auto &legalMoves = gamePlayfield.GetLegalMoves(cx, cy, true);
                gamePlayfield.selected = true;
                gamePlayfield.selectedX = cx;
                gamePlayfield.selectedY = cy;
                w.Redraw();
            }
        }
    };

    gameWindow.width = gameViewport.width + 12;
    gameWindow.height = gameViewport.height + 32;
    while (!gameWindow.isClosed)
        Task::Switch();
    gameWindow.Kill();
    return 0;
}
